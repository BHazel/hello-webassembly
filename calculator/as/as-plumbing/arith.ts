export function add(num1: f64, num2: f64): f64 {
  return num1 + num2;
}

export function subtract(num1: f64, num2: f64): f64 {
  return num1 - num2;
}

export function multiply(num1: f64, num2: f64): f64 {
  return num1 * num2;
}

export function divide(num1: f64, num2: f64): f64 {
  return num1 / num2;
}